// Slider functionality
document.addEventListener('DOMContentLoaded', function() {
    // Mobile menu functionality
    const mobileMenuButton = document.querySelector('.mobile-menu');
    const nav = document.querySelector('nav');
    
    if (mobileMenuButton && nav) {
        mobileMenuButton.addEventListener('click', function() {
            mobileMenuButton.classList.toggle('active');
            nav.classList.toggle('active');
            document.body.style.overflow = nav.classList.contains('active') ? 'hidden' : '';
        });

        // Close menu when clicking outside
        document.addEventListener('click', function(event) {
            if (nav.classList.contains('active') && 
                !nav.contains(event.target) && 
                !mobileMenuButton.contains(event.target)) {
                mobileMenuButton.classList.remove('active');
                nav.classList.remove('active');
                document.body.style.overflow = '';
            }
        });
    }

    // Initialize main page plants if we're on the main page
    if (document.querySelector('.container_card')) {
        displayPlantsOnMainPage();
    }

    // Slider functionality - only run on pages with slider
    const track = document.querySelector('.slider-track');
    if (track) {
        initializeSlider(track);
    }

    // Initialize catalog if we're on the catalog page
    if (document.querySelector('.catalog-grid')) {
        displayPlantsInCatalog();
        setupCategoryFilters();
    }
});

// Slider functionality
function initializeSlider(track) {
    const slides = Array.from(track.children);
    const nextButton = document.querySelector('.slider-button.next');
    const prevButton = document.querySelector('.slider-button.prev');
    let currentIndex = 0;

    function getSlidesPerView() {
        if (window.innerWidth <= 768) return 1;
        if (window.innerWidth <= 1024) return 2;
        return 3;
    }

    function updateSlideWidth() {
        const slidesPerView = getSlidesPerView();
        const slideWidth = track.clientWidth / slidesPerView;
        slides.forEach(slide => {
            slide.style.width = `${slideWidth}px`;
        });
    }

    function updateSliderPosition() {
        const slidesPerView = getSlidesPerView();
        const slideWidth = track.clientWidth / slidesPerView;
        track.style.transform = `translateX(-${currentIndex * slideWidth}px)`;
    }

    function updateButtons() {
        const slidesPerView = getSlidesPerView();
        prevButton.disabled = currentIndex === 0;
        nextButton.disabled = currentIndex >= slides.length - slidesPerView;
    }

    nextButton.addEventListener('click', () => {
        const slidesPerView = getSlidesPerView();
        if (currentIndex < slides.length - slidesPerView) {
            currentIndex++;
            updateSliderPosition();
            updateButtons();
        }
    });

    prevButton.addEventListener('click', () => {
        if (currentIndex > 0) {
            currentIndex--;
            updateSliderPosition();
            updateButtons();
        }
    });

    window.addEventListener('resize', () => {
        updateSlideWidth();
        updateSliderPosition();
        updateButtons();
    });

    // Initial setup for slider
    updateSlideWidth();
    updateButtons();
}

// Function to display plants on main page
function displayPlantsOnMainPage() {
    const containerCards = document.querySelectorAll('.container_card');
    
    containerCards.forEach((container) => {
        // Clear existing content
        container.innerHTML = '';
        
        // Get 4 random plants from the array
        const randomPlants = [...plants]
            .sort(() => Math.random() - 0.5)
            .slice(0, 4);
        
        // Create cards for each plant
        randomPlants.forEach(plant => {
            const card = document.createElement('div');
            card.className = 'card';
            
            const imageName = plant.englishName.replace(/\s+/g, '');
            card.innerHTML = `
                <div class="card_img">
                    <img src="${plant.img}">
                </div>
                <div class="card_desc">
                    <div>
                        <h2>${plant.name}</h2>
                        <p>${plant.englishName}</p>
                    </div>
                    <button>Узнать больше</button>
                </div>
            `;
            
            // Add click event to the button
            const button = card.querySelector('button');
            button.addEventListener('click', () => {
                // Store the selected plant data
                localStorage.setItem('selectedPlant', JSON.stringify(plant));
                // Navigate to product detail page
                window.location.href = 'product-detail.html';
            });
            
            container.appendChild(card);
        });
    });
}

// Catalog functionality
function displayPlantsInCatalog(filteredPlants = null, currentPage = 1) {
    const catalogGrid = document.querySelector('.catalog-grid');
    if (!catalogGrid) return;

    // Очищаем существующую пагинацию
    const existingPagination = document.querySelector('.pagination');
    if (existingPagination) {
        existingPagination.remove();
    }

    const plantsToDisplay = filteredPlants || plants;
    catalogGrid.innerHTML = '';

    // Настройки пагинации
    const itemsPerPage = 6;
    const start = (currentPage - 1) * itemsPerPage;
    const end = start + itemsPerPage;
    const paginatedPlants = plantsToDisplay.slice(start, end);

    // Создаем карточки растений
    paginatedPlants.forEach(plant => {
        const plantCard = document.createElement('div');
        plantCard.className = 'plant-card';
        plantCard.innerHTML = `
            <div class="img_container">
                 <img src="${plant.img}" >  
            </div>
     <div class="container_pland__desk">
         <h3>${plant.name}</h3>
            <p class="english-name">${plant.englishName}</p>
            <button class="learn-more" data-id="${plants.indexOf(plant)}">Узнать больше</button>
</div>
        
        `;
        catalogGrid.appendChild(plantCard);

        // Добавляем обработчик для кнопки "Узнать больше"
        const learnMoreBtn = plantCard.querySelector('.learn-more');
        learnMoreBtn.addEventListener('click', () => {
            const plantId = learnMoreBtn.getAttribute('data-id');
            window.location.href = `product-detail.html?id=${plantId}`;
        });
    });

    // Добавляем пагинацию
    const totalPages = Math.ceil(plantsToDisplay.length / itemsPerPage);
    if (totalPages > 1) {
        const paginationContainer = document.createElement('div');
        paginationContainer.className = 'pagination';

        for (let i = 1; i <= totalPages; i++) {
            const pageButton = document.createElement('button');
            pageButton.textContent = i;
            if (i === currentPage) {
                pageButton.classList.add('active');
            }
            pageButton.addEventListener('click', () => {
                displayPlantsInCatalog(plantsToDisplay, i);
            });
            paginationContainer.appendChild(pageButton);
        }

        catalogGrid.parentElement.appendChild(paginationContainer);
    }
}

// Category filtering functionality
function setupCategoryFilters() {
    const categoryItems = document.querySelectorAll('.sidebar ul li');
    if (!categoryItems.length) return;

    categoryItems.forEach(item => {
        item.addEventListener('click', function() {
            // Remove active class from all items
            categoryItems.forEach(cat => cat.classList.remove('active'));
            // Add active class to clicked item
            this.classList.add('active');

            const category = this.textContent.toLowerCase();
            let filteredPlants;

            if (category === 'все растения') {
                filteredPlants = plants;
            } else {
                filteredPlants = plants.filter(plant => {
                    return plant.category.toLowerCase() === category;
                });
            }

            displayPlantsInCatalog(filteredPlants);
        });
    });
}

